Cypress.Commands.add('login', ({email, password}) => {
  cy.get('input[type="email"]').type(email);
  cy.get('input[type="password"]').type(password);
  cy.get('button[type="submit"]').click();
});

Cypress.Command.add('logout', () => {
  cy.get('.navbar > div')
    .contains('Logout')
    .click();
});
